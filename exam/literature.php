<b><i>Hyderabad Central University:-</b></i>The State Central Library Hyderabad,better known as the State Central Library (SCL), is a public library in Hyderabad, Telangana. The building was constructed in 1891 by Nawab Imad-ul-Mulk. Formerly known as the Asafia Library, it is one of the most imposing structures in the city and was granted heritage status in 1998 by INTACH, Hyderabad.
<br><br>
The library is located at Afzal Gunj on the bank of the River Musi. It houses 500,000 books and magazines including some rare palmleaf manuscripts. This library is the apex of the state's library system.
