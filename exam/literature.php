<b><i>Jadavpur Universuty:-</b></i>The Department of Comparative Literature (Comp. Lit.) was started in the year 1956 and enjoys the reputation of a distinguished department to offer a subject which is distinct not only in India, but also in Asia. For the last two decades, the department has widened its sphere of learning to all-important Indian languages for comparative studies. It also exerts special emphases on African, Latin American and Australian Literature that are otherwise neglected in the taught programmes of the major departments of the Indian Universities. 
