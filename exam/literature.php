<b><i>Delhi University:-</b></i>The Department of Modern Indian Languages and Literary Studies was established in the year 1961.  It runs Master Programmes in Bengali, Comparative Indian Literature and Tamil, and Certificate - Diploma - Advanced Diploma courses in 11 Indian Languages. The research programme in the field of Comparative Indian Literature, Folklore and Indian Languages and Literature, initiated by the Department, have drawn the attention of scholars from India and abroad. The Department has produced a good number of scholars in these fields during these years. The Master programme in Comparative Indian Literature has also come to limelight for its unique design and its relevance in the multi-lingual and multi-cultural situation of India, where a wide variety of literatures are produced in different languages.
<br><br>
Department has also undertaken various initiatives under the ongoing UGC DRS assistance. Under this scheme, the thrust areas are: Comparative Indian Literature, Translation Studies and Indian Folklore and Tribal lore.  A series of National level seminars on these thrust areas have been organized during these years and few volumes are also to be published soon.
